import { METHODS, METHOD_GAME_MAP } from './methods.js'

function expand(raw) {
  const [id, name, gen, status, methodsStr, notes] = raw

  let methods = []
  let games = []

  if (methodsStr) {
    const keys = methodsStr.split(',').map(k => k.trim())
    methods = keys
      .map(k => METHODS[k])
      .filter(Boolean)
      .sort((a, b) => a.denominator - b.denominator)

    const gameSet = new Set()
    methods.forEach(m => {
      const g = METHOD_GAME_MAP[m.id] || []
      g.forEach(gk => gameSet.add(gk))
    })
    games = [...gameSet]
  }

  return { id, name, gen, status, methods, games, notes: notes || null }
}

export const POKEMON = RAW.map(expand)
